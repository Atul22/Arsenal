package DataObjects;

public class EPLPlayersObject {
    private String playerPosition;
    private String playerJersey;
    private String playerName;
    private String playerNationality;
    private String playerDOB;
    private String playerContract;

    public String getPlayerPosition() {
        return playerPosition;
    }

    public void setPlayerPosition(String playerPosition) {
        this.playerPosition = playerPosition;
    }

    public String getPlayerJersey() {
        return playerJersey;
    }

    public void setPlayerJersey(String playerJersey) {
        this.playerJersey = playerJersey;
    }

    public String getPlayerName() {
        return playerName;
    }

    public void setPlayerName(String playerName) {
        this.playerName = playerName;
    }

    public String getPlayerNationality() {
        return playerNationality;
    }

    public void setPlayerNationality(String playerNationality) {
        this.playerNationality = playerNationality;
    }

    public String getPlayerDOB() {
        return playerDOB;
    }

    public void setPlayerDOB(String playerDOB) {
        this.playerDOB = playerDOB;
    }

    public String getPlayerContract() {
        return playerContract;
    }

    public void setPlayerContract(String playerContract) {
        this.playerContract = playerContract;
    }
}
