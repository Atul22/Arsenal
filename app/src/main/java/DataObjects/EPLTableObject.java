package DataObjects;

public class EPLTableObject {
    private String position;
    private String logo;
    private String teamName;
    private String gamesPlayed;
    private String gamesWon;
    private String gamesLost;
    private String goalDiff;
    private String points;

    public void setPosition(String position) {
        this.position = position;
    }

    public String getPosition() {
        return position;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public String getLogo() {
        return logo;
    }

    public void setTeamName(String teamName) {
        this.teamName = teamName;
    }

    public String getTeamName() {
        return teamName;
    }

    public void setGamesPlayed(String gamesPlayed) {
        this.gamesPlayed = gamesPlayed;
    }

    public String getGamesPlayed() {
        return gamesPlayed;
    }

    public void setGamesWon(String gamesWon) {
        this.gamesWon = gamesWon;
    }

    public String getGamesWon() {
        return gamesWon;
    }

    public void setGamesLost(String gamesLost) {
        this.gamesLost = gamesLost;
    }

    public String getGamesLost() {
        return gamesLost;
    }

    public void setGoalDiff(String goalDiff) {
        this.goalDiff = goalDiff;
    }

    public String getGoalDiff() {
        return goalDiff;
    }

    public void setPoints(String points) {
        this.points = points;
    }

    public String getPoints() {
        return points;
    }
}
